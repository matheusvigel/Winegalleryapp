// Helper function to get all regions flattened
export function getAllRegions() {
  return countries.flatMap(country => country.regions);
}

// Alias for backward compatibility
export const regions = getAllRegions();

// Helper function to find a region by ID
export function findRegion(regionId: string) {
  for (const country of countries) {
    const region = country.regions.find(r => r.id === regionId);
    if (region) return region;
  }
  return null;
}

// Helper function to find a country by ID
export function findCountry(countryId: string) {
  return countries.find(c => c.id === countryId);
}

// Helper function to find a brand by ID
export function findBrand(brandId: string) {
  return brands.find(b => b.id === brandId) || null;
}

// Helper function to find a grape by ID
export function findGrape(grapeId: string) {
  return grapes.find(g => g.id === grapeId) || null;
}
